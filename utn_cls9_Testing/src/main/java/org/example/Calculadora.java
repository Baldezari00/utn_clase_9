package org.example;

public class Calculadora {
    public double sumar(double unNum, double otroNum){
        return unNum+otroNum;
    }

    public double restar(double unNum, double otroNum){
        return unNum-otroNum;
    }

    public double multiplicar(double unNum, double otroNum){
        return unNum*otroNum;
    }
    public double dividir(double unNum, double otroNum){
        return unNum/otroNum;
    }
}
